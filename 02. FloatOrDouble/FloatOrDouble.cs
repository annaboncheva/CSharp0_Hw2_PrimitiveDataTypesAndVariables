using System;

class FloatOrDouble
{
    static void Main()
    {
        Console.Title = "Float or Double";
        double a = 34.567839023;
        float b = 12.345f;
        double c = 8923.1234857;
        float d = 3456.091f;
        Console.WriteLine("{0}\t{1}\t{2}\t{3}", a, b, c, d);
    }
}

