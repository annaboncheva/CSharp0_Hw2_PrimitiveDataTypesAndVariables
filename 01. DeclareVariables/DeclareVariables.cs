using System;

class DeclareVariables
{
    static void Main()
    {
        Console.Title = "Declare Variables";
        ushort a = 52130;
        sbyte b = -115;
        uint c = 4825932;
        byte d = 97;
        short e = -10000;
        Console.Write("{0}\tushort\n{1}\tsbyte\n{2}\tuint\n{3}\tbyte\n{4}\tshort\n", a, b, c, d, e);
    }
}

